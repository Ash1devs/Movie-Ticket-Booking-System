    private void loadAllBookings(String filter) {
    String[] columns = {
        "BookingID", "Movie", "Cinema",
        "Date & Time", "Seats", "Amount", "Status", "Action"
    };
    
    javax.swing.table.DefaultTableModel model =
        new javax.swing.table.DefaultTableModel(columns, 0) {
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
    
    for (String[] record : DataStoreU.allBookings) {
        // Filter logic
        if (filter.equals("All") || record[7].equals(filter)) {
            model.addRow(new String[]{
                record[0], record[1], record[2],
                record[3], record[4], record[5],
                record[7], "Cancel"
            });
        }
    }
    
    // Replace tblManageBookings with YOUR JTable variable name
    tblManageBookings.setModel(model);
    
    // Show count
    jLabel1.setText("Total: " + model.getRowCount() + " bookings");
}

