   private void updateStatusLabel() {
    int row = tblBookings.getSelectedRow();
    if (row != -1) {
        String status = tblBookings.getValueAt(row, 7).toString();
        lblStatusDisplay.setText(status); // 'lblStatusDisplay' is the label next to "Booking:"
        
        if (status.equalsIgnoreCase("Confirmed")) {
            lblStatusDisplay.setForeground(java.awt.Color.GREEN);
        } else {
            lblStatusDisplay.setForeground(java.awt.Color.RED);
        }
    }
}
