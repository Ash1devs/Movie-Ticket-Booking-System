private void loadTable() {
    String[] columns = {"ID", "Title", "Genre", "Duration", "Rating"};
    
    javax.swing.table.DefaultTableModel model =
        new javax.swing.table.DefaultTableModel(columns, 0) {
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
    
    for (Movie m : DataStoreU.movies) {
        model.addRow(new Object[]{
            m.id, m.title, m.genre, m.duration, m.rating
        });
    }
    
    tblMovies.setModel(model);
    tblMovies.setRowHeight(30);
}

