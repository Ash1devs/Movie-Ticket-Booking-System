    private void loadHistory() {
    String[] columns = {
        "Booking ID", "Movie", "Cinema",
        "Date & Time", "Seats", "Amount",
        "Payment", "Status"
    };
    
    javax.swing.table.DefaultTableModel model = 
        new javax.swing.table.DefaultTableModel(columns, 0){
     public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
    
         for (String[] record : DataStoreU.allBookings) {
        // Only show first 7 columns (not payment method)
        model.addRow(new String[]{
            record[0], // BookingID
            record[1], // Movie
            record[2], // Cinema
            record[3], // Date & Time
            record[4], // Seats
            record[5], // Amount
            record[7]  // Status
        });
    }
    
    // Replace tblBookings with YOUR JTable variable name
    tblBookings.setModel(model);
    lblLoggedIn.setText("Logged in as: " + DataStoreU.email);
}

