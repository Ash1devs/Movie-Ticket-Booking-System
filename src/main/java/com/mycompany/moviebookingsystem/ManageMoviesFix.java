    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      
        int row = tblMovies.getSelectedRow();
    if (row < 0) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Click a movie row first!");
        return;
    }
    
    Movie m = DataStoreU.movies.get(row);
    
    // Create input fields pre-filled with current data
    javax.swing.JTextField fTitle = 
        new javax.swing.JTextField(m.title, 20);
    javax.swing.JTextField fGenre = 
        new javax.swing.JTextField(m.genre, 20);
    javax.swing.JTextField fDuration = 
        new javax.swing.JTextField(m.duration, 20);
    javax.swing.JTextField fRating = 
        new javax.swing.JTextField(m.rating, 20);
    javax.swing.JTextField fDesc = 
        new javax.swing.JTextField(m.description, 20);
    
    // Build dialog panel
    javax.swing.JPanel panel = new javax.swing.JPanel(
        new java.awt.GridLayout(0, 2, 5, 10));
    panel.add(new javax.swing.JLabel("Title:"));
    panel.add(fTitle);
    panel.add(new javax.swing.JLabel("Genre:"));
    panel.add(fGenre);
    panel.add(new javax.swing.JLabel("Duration:"));
    panel.add(fDuration);
    panel.add(new javax.swing.JLabel("Rating:"));
    panel.add(fRating);
    panel.add(new javax.swing.JLabel("Description:"));
    panel.add(fDesc);
    
    int result = javax.swing.JOptionPane.showConfirmDialog(
        this, panel,
        "Edit Movie: " + m.title,
        javax.swing.JOptionPane.OK_CANCEL_OPTION,
        javax.swing.JOptionPane.PLAIN_MESSAGE);
    
    if (result == javax.swing.JOptionPane.OK_OPTION) {
        // Update movie in memory
        m.title = fTitle.getText();
        m.genre = fGenre.getText();
        m.duration = fDuration.getText();
        m.rating = fRating.getText();
        m.description = fDesc.getText();
        
        // Save to file
        DataStoreU.saveMoviesToFile();
        
        // Refresh table immediately
        loadTable();
        
        javax.swing.JOptionPane.showMessageDialog(this,
            "Movie updated successfully!");
    } 
        // Save to file
        
    // User Information (for UserDetailsScreen)
 // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

