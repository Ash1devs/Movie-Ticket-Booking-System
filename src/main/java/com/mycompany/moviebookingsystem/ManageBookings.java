    private void tblManageBookingsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManageBookingsMouseClicked
      int row = tblManageBookings.getSelectedRow();
    if (row >= 0) {
        String bookingId = tblManageBookings.getValueAt(row, 0).toString();
        
        // Find in DataStore and cancel
        for (String[] record : DataStoreU.allBookings) {
            if (record[0].equals(bookingId)) {
                int confirm = javax.swing.JOptionPane.showConfirmDialog(
                    this,
                    "Cancel booking " + bookingId + "?",
                    "Confirm Cancellation",
                    javax.swing.JOptionPane.YES_NO_OPTION
                );
                if (confirm == javax.swing.JOptionPane.YES_OPTION) {
                    record[7] = "Cancelled";
                    loadAllBookings("All");
                }
                break;
            }
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_tblManageBookingsMouseClicked

