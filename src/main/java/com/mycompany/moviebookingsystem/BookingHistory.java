    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
     int selectedRow = tblBookings.getSelectedRow();
    
    if (selectedRow != -1) {
        // Update the "Status" (Column index 7) in your DataStoreU list
        String[] record = DataStoreU.allBookings.get(selectedRow);
        record[7] = "Cancelled"; 
        
        // Refresh the table to show the change
        loadHistory(); 
        
        // Update the status label color immediately
        updateStatusLabel();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a booking to cancel.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

